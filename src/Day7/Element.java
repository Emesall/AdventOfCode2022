package Day7;

public abstract class Element {


	private String name;
	private long size;

	
	
	


	public Element(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getSize() {
		return size;
	}

	public void setSize(long size) {
		this.size = size;
	}

	public abstract long calculateSize();

}
